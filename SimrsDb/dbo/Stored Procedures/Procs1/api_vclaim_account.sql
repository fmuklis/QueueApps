CREATE PROCEDURE [dbo].[api_vclaim_account]
	-- Add the parameters for the stored procedure here
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;

    -- Insert statements for procedure here
	SELECT a.protocol, a.baseUrl, a.consId, a.[secret], a.ppk, a.ppkNama, a.token AS userKey
	  FROM dbo.apiBridgeAccount a
	 WHERE idApi = 2/*VClaim*/
END