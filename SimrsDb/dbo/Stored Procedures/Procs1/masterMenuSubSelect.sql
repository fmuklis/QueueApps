CREATE PROCEDURE [dbo].[masterMenuSubSelect]
	-- Add the parameters for the stored procedure here

AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;

    -- Insert statements for procedure here
	SELECT a.idSubMenu, b.namaIconMenu, b.namaMenu, a.namaIconSubMenu, a.namaSubMenu, a.urutan, a.URL
	  FROM dbo.masterMenuSub a
		   Left Join dbo.masterMenu b On a.idMenu = b.idMenu
END